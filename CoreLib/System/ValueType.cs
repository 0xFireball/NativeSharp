namespace System
{
    using System;
    using System.Reflection;
    using System.Runtime.CompilerServices;

    [Serializable]
    public abstract class ValueType
    {
        protected static int GetHashCodeOfPtr(IntPtr argCookie)
        {
            return argCookie.ToInt32();
        }
    }
}


